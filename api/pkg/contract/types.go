package contract

import (
	"context"
)

type Contract interface {
	ArtistComplete(
		ctx context.Context,
		id string,
	) error

	ArtistCancelled(
		ctx context.Context,
		id string,
	) error

	ImageComplete(
		ctx context.Context,
		id int,
		result string,
	) error

	ImageCancelled(
		ctx context.Context,
		id int,
		errorString string,
	) error

	GetImageIDs(
		ctx context.Context,
	) ([]int, error)

	GetArtistIDs(
		ctx context.Context,
	) ([]string, error)

	GetImage(
		ctx context.Context,
		id int,
	) (ArtistAttributionImage, error)
}
